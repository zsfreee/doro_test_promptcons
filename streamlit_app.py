        visible_part = ""
        hidden_part = ""
        
        if visible_marker in split_answer and hidden_marker in split_answer:
            visible_part = split_answer.split(visible_marker)[1].split(hidden_marker)[0].strip()
            hidden_part = split_answer.split(hidden_marker)[1].strip()
